class ZipData2<A, B> {
  final A first;
  final B second;

  ZipData2(this.first, this.second);
}

class ZipData3<A, B, C> {
  final A first;
  final B second;
  final C third;

  ZipData3(this.first, this.second, this.third);
}

class ZipData4<A, B, C, D> {
  final A first;
  final B second;
  final C third;
  final D forth;

  ZipData4(this.first, this.second, this.third, this.forth);
}
